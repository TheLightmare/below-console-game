void BitmapFont::create_builtin(VkContext& ctx) {
    glyph_w_ = 8;
    glyph_h_ = 8;
    tex_w_   = 128; // 16 cols * 8px
    tex_h_   = 128; // 16 rows * 8px

    // Build RGBA texture (128x128)
    std::vector<uint8_t> pixels(tex_w_ * tex_h_ * 4, 0);

    for (int ch = 32; ch <= 126; ++ch) {
        int idx   = ch - 32;
        int col   = ch % 16;
        int row   = ch / 16;
        int base_x = col * 8;
        int base_y = row * 8;

        for (int py = 0; py < 8; ++py) {
            uint8_t scanline = BUILTIN_FONT_8X8[idx][py];
            for (int px = 0; px < 8; ++px) {
                bool set = (scanline >> (7 - px)) & 1;
                int ti   = ((base_y + py) * static_cast<int>(tex_w_) + (base_x + px)) * 4;
                pixels[ti + 0] = 255;           // R
                pixels[ti + 1] = 255;           // G
                pixels[ti + 2] = 255;           // B
                pixels[ti + 3] = set ? 255 : 0; // A
            }
        }
    }

    texture_.create_from_pixels(ctx, pixels.data(), tex_w_, tex_h_, false);
}

// =============================================================================
// Draw text
// =============================================================================

void BitmapFont::draw_text(SpriteBatch& batch,
                            VkDescriptorSet font_texture_set,
                            float x, float y,
                            const std::string& text,
                            const Color4& color,
                            float scale) {
    float cx = x;
    float gw = glyph_w_ * scale;
    float gh = glyph_h_ * scale;

    for (char ch : text) {
        if (ch == '\n') {
            cx = x;
            y += gh;
            continue;
        }

        UVRect uv = glyph_uv(ch);
        batch.draw(font_texture_set, { cx, y, gw, gh }, uv, color);
        cx += gw;
    }
}

float BitmapFont::measure_width(const std::string& text, float scale) const {
    float max_w = 0;
    float cur_w = 0;
    float gw    = glyph_w_ * scale;

    for (char ch : text) {
        if (ch == '\n') {
            if (cur_w > max_w) max_w = cur_w;
            cur_w = 0;
        } else {
            cur_w += gw;
        }
    }
    return cur_w > max_w ? cur_w : max_w;
}

float BitmapFont::line_height(float scale) const {
    return glyph_h_ * scale;
}

UVRect BitmapFont::glyph_uv(char ch) const {
    uint8_t c = static_cast<uint8_t>(ch);
    int col = c % 16;
    int row = c / 16;

    float u0 = static_cast<float>(col * glyph_w_) / tex_w_;
    float v0 = static_cast<float>(row * glyph_h_) / tex_h_;
    float u1 = static_cast<float>((col + 1) * glyph_w_) / tex_w_;
    float v1 = static_cast<float>((row + 1) * glyph_h_) / tex_h_;

    return { u0, v0, u1, v1 };
}

void BitmapFont::destroy(VkContext& ctx) {
    texture_.destroy(ctx);
}
