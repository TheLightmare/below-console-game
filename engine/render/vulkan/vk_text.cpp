// =============================================================================
// Load from file
// =============================================================================

bool BitmapFont::load(VkContext& ctx, const char* filepath,
                       uint32_t glyph_w, uint32_t glyph_h) {
    glyph_w_ = glyph_w;
    glyph_h_ = glyph_h;

    if (!texture_.load_from_file(ctx, filepath, false)) {
        return false;
    }

    tex_w_ = texture_.width();
    tex_h_ = texture_.height();
    return true;
}

